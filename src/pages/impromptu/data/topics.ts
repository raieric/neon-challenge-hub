export const ALL_CATEGORIES = TOPIC_CATEGORIES.map((c) => c.name);

export function getRandomTopic(
  category?: string,
  lastTopic?: string
): { topic: string; category: string } {
  const pool = category
    ? TOPIC_CATEGORIES.filter((c) => c.name === category)
    : TOPIC_CATEGORIES;

  const allTopics = pool.flatMap((c) =>
    c.topics.map((t) => ({ topic: t, category: c.name }))
  );

  let pick = allTopics[Math.floor(Math.random() * allTopics.length)];
  // Avoid immediate repetition
  let attempts = 0;
  while (pick.topic === lastTopic && attempts < 10) {
    pick = allTopics[Math.floor(Math.random() * allTopics.length)];
    attempts++;
  }
  return pick;
}
